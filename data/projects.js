// Helper functions to get all projects and filter them
export const getAllProjects = () => {
  const allProjects = []
  Object.keys(projectsData).forEach((country) => {
    Object.keys(projectsData[country]).forEach((sector) => {
      projectsData[country][sector].forEach((project) => {
        allProjects.push({
          ...project,
          country,
          sector,
        })
      })
    })
  })
  return allProjects
}

export const getProjectsByCountry = (country) => {
  const projects = []
  if (projectsData[country]) {
    Object.keys(projectsData[country]).forEach((sector) => {
      projectsData[country][sector].forEach((project) => {
        projects.push({
          ...project,
          country,
          sector,
        })
      })
    })
  }
  return projects
}

export const getProjectsBySector = (sector) => {
  const projects = []
  Object.keys(projectsData).forEach((country) => {
    if (projectsData[country][sector]) {
      projectsData[country][sector].forEach((project) => {
        projects.push({
          ...project,
          country,
          sector,
        })
      })
    }
  })
  return projects
}

export const getProjectById = (id) => {
  const allProjects = getAllProjects()
  return allProjects.find((project) => project.id === id)
}

export const searchProjects = (query) => {
  const allProjects = getAllProjects()
  return allProjects.filter(
    (project) =>
      project.project_name.includes(query) ||
      project.description.includes(query) ||
      project.location.includes(query) ||
      project.country.includes(query),
  )
}

